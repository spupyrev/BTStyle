string unicode_latex::transform(const string& s)
{
	string res;

    size_t i = 0;
    while (i < s.size())
    {
		string cur;
        unsigned long uni;
        size_t todo;
        unsigned char ch = s[i++];
		cur += ch;
        if (ch <= 0x7F)
        {
            uni = ch;
            todo = 0;
        }
        else if (ch <= 0xBF)
        {
			Logger::Warning("not a UTF-8 string: '" + s + "'");
			return s;
        }
        else if (ch <= 0xDF)
        {
            uni = ch&0x1F;
            todo = 1;
        }
        else if (ch <= 0xEF)
        {
            uni = ch&0x0F;
            todo = 2;
        }
        else if (ch <= 0xF7)
        {
            uni = ch&0x07;
            todo = 3;
        }
        else
        {
			Logger::Warning("not a UTF-8 string: '" + s + "'");
			return s;
        }

        for (size_t j = 0; j < todo; j++)
        {
            if (i == s.size())
			{
				Logger::Warning("not a UTF-8 string: '" + s + "'");
				return s;
			}
            unsigned char ch = s[i++];
			cur += ch;
            if (ch < 0x80 || ch > 0xBF)
			{
				Logger::Warning("not a UTF-8 string: '" + s + "'");
				return s;
			}
            uni <<= 6;
            uni += ch & 0x3F;
        }

        if (uni >= 0xD800 && uni <= 0xDFFF)
		{
			Logger::Warning("not a UTF-8 string: '" + s + "'");
			return s;
		}
        if (uni > 0x10FFFF)
		{
			Logger::Warning("not a UTF-8 string: '" + s + "'");
			return s;
		}

		if (todo > 0 && substMap.count(uni))
			res += substMap.find(uni)->second;
		else
			res += cur;
    }

	return res;
}

string unicode_latex::transform(char c1, char c2)
{
	unsigned long uni;
	uni = c1 & 0x0F;
	uni <<= 6;
    uni += c2 & 0x3F;

	if (substMap.count(uni))
		return substMap.find(uni)->second;

	string res;
	res += c1;
	res += c2;
	return res;
}

